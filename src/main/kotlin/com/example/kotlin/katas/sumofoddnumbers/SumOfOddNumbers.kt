package com.example.kotlin.katas.sumofoddnumbers

/**
 * Based on Code-wars kata:
 * https://www.codewars.com/kata/55fd2d567d94ac3bc9000064/kotlin
 */
class SumOfOddNumbers {

    /**
     * Sum odd numbers from given row number
     * Based on explanation https://planetmath.org/sumofoddnumbers
     */
    fun sumOfOddNumbers(n: Int): Int {
        return n * n * n
    }
}
